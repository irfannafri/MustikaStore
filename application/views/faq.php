			<p>
				<b>The combination lock on my case won&rsquo;t open, how can I open it without damaging or removing the lock?</b></p>
			<p>
				If the combination was somehow changed, try shining a flashlight into the spaces where the thumbwheels turn. As you rotate the wheel, you&#39;ll see a small notch once it gets to the correct number. Line up all three thumbwheels in this way, and it should open.</p>
			<p>
				You can also use a thin strip of metal if you have trouble using the flashlight method. Using a thin strip of metal &ndash; (we suggest one cut carefully from an aluminum can) - to insert into the space beside each thumbwheel - hold pressure on the metal strip as you rotate the wheel - you&#39;ll feel the notch as you rotate - stop when you feel the notch and move on to the next wheel until you have all three set.</p>
			<p>
				The long way around this is to start at &quot;000&quot; and work through the 1,000 combinations - it can take a few hours, but it does work!</p>
			<p>
				If the lock is seems stuck or is actually damaged, we do recommend having a locksmith open/repair the damaged lock.</p>
			<p>
				Below are listed the instructions for resetting the lock combination:</p>
			<p>
				The locks are set at the factory to open at 0-0-0.<br />
				To set your own combination;<br />
				<b>1)</b> Choose 3 digits which you can recall easily-address, phone, birthday, etc.<br />
				<b>2)</b> Press the button in a direction toward the dial with your thumb and hold with your other hand (Use point of ballpoint pen) rotate the dials to the desired setting. Then, release the button.<br />
				<b>3)</b> Your lock is now set to open on the three digits engaged at the time you release the button.<br />
				<b>CAUTION!</b><br />
				<b>4)</b> Accordingly- NEVER rotate the dials with the button held open UNLESS you make careful note of the dial setting upon release of the button.<br />
				<b>5)</b> The manufacturer is not responsible for damage resulting from misuse, abusive handling, or failure to follow instructions.<br />
				Remove the red arrow before setting the lock.<br />
				<b>* Please note:</b> Make sure to cut down the red plastic pin before operation.</p>
