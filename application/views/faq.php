			<p>
				<b>My instrument has small cracks in the finish &ndash; what causes this? Is it covered under the warranty?</b></p>
			<p>
				This is known as &#39;finish checking&#39;. This is a result of the instrument being subjected to a rapid change in temperature or humidity. In most cases it happens in winter when a chilled instrument is exposed to warm air. The cracks are a result of the wood expanding faster than the lacquer. Since we have no control over the environmental conditions surrounding the use of the instrument we cannot cover this type of situation under our warranty.</p>
		</li>
		<li>
			<p>
				<b>What is &ldquo;stand damage?&rdquo;</b></p>
			<p>
				Stand damage occurs when the chemicals in the rubber used on guitar stands react with the nitrocellulose lacquer used to finish our instruments. It can range from a slight discoloration to the finish to actually &quot;eating away&quot; the lacquer topcoat. Unfortunately, this situation is not covered under your warranty. The safest way to prevent this from happening is to cover the rubber parts of your stand with a soft cotton cloth (use guitar polishing cloths). Guitar stands should only be used to &#39;temporarily&#39; store your instrument, such as on a gig. The safest place to store your instrument is in its case.</p>
		</li>
		<li>
			<p>
				<b>What can I do to prevent tarnish on the hardware?</b></p>
			<p>
				The tarnish on hardware occurs due to a reaction between the plating and moisture, either in the air or from perspiration. Keep in mind that all hardware will tarnish eventually- it is not a sign that your hardware is &quot;cheap.&quot; In some areas there is low moisture content in the air so the tarnishing process will be slower, but it will eventually happen. You can slow down the tarnishing process by wiping down the instrument every time you play it! This one step will add life to the hardware and slow down the tarnishing process. Also, make sure you store the instrument in its&#39; case to prevent it from exposure to high moisture content level in the air.</p>
